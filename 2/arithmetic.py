print(2 + 3)

print(2 * 3)

# 0.6666666666666666
print(2 / 3)

# 乘方：8
print(2 ** 3)

# 浮点运算
print(0.1 * 2)
print(0.1 + 0.1)

# 0.30000000000000004
print(0.1 + 0.2)

# 0.30000000000000004
print(0.1 * 3)

# 0.010000000000000002
print(0.1 * 0.1)

# 整数和浮点：2.0
print(4 / 2)

# 3.0
print(1 + 2.0)

# 6.0
print(3.0 * 2)

# 9.0
print(3.0 ** 2)

# 数中的下划线
# 仅在 Python 3.6 和更高版本下支持
universe_age = 14_000_000_000
print(universe_age)

# 同时支持浮点类型
atomic_nucleus = 0.000_000_000_000_01
print(atomic_nucleus)

# 多变量赋值
x, y, z = 1, 2, 3
print(x, y, z)
